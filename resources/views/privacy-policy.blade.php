                        <div class="single-description-para" id="what-is-personal-information">
                            <h6>What is personal information?</h6>
                            <p class="text-justify">Personal information is information about an identifiable
                                individual. It includes (but is not limited to) name, address, contact details, date of
                                birth, occupations, payment details, employment history and/or details, education and
                                qualifications, financial information, testimonials and feedback, evidence of source of
                                funds or source of wealth (in some cases) and other information.</p>
                        </div>
                        <div class="single-description-para" id="we-will-use-your-personal-information">
                            <h6>We will use your personal information:</h6>
                            <ul>
                                <li>to verify your identity</li>
                                <li>to provide services and products to you</li>
                                <li>to market our services and products to you, including contacting you electronically
                                    (e.g. by call, text, or email for this purpose)</li>
                                <li>to improve the services and products that we provide to you</li>
                                <li>to respond to communications from you, including a complaint</li>
                                <li>to protect and/or enforce our legal rights and interests, including defending any
                                    claim</li>
                                <li>for any other purpose authorised by you or the Act.</li>
                            </ul>
                        </div>
                        <div class="single-description-para" id="disclosing-your-personal-information">
                            <h6>Disclosing your personal information</h6>
                            <p>We may disclose your personal information to:</p>
                            <ul>
                                <li> Other companies or individuals who assist us in providing services or who perform
                                    functions on our behalf (such as mailing houses, hosting and data storage providers,
                                    specialist consultants, and legal advisers);</li>
                                <li> Product providers (such as but not limited to lenders and insurance companies);
                                </li>
                                <li> Financial advisers and financial advice providers who may use our services;</li>
                                <li> Other companies or individuals who perform checks (such as but not limited to
                                    compliance reviews and audits) that are necessary or desirable under the law on our
                                    behalf;</li>
                                <li> Other companies, agencies, or individuals that maintain databases against which
                                    your identity may be verified, which may include (but are not limited to) the New
                                    Zealand Department of Internal Affairs, and New Zealand Transport Agency;</li>
                                <li> Social media sites on which we may have a presence;</li>
                                <li> Courts, tribunals, and regulatory authorities (such as the Financial Markets
                                    Authority, and the Ministry of Justice in New Zealand);</li>
                                <li> Office of the Ombudsman, where a complaint relates to official information;</li>
                                <li> Any person or agency we believe could assist in responding to a serious privacy
                                    breach.</li>
                                <li> Office of the New Zealand Privacy Commissioner, where a complaint relates to breach
                                    of the Privacy Act 2020;</li>
                                <li> Human Rights Commission, where a complaint relates to discrimination;</li>
                                <li> CERT NZ, where appropriate to assist with the management of a voluntarily notified
                                    privacy breach</li>
                                <li>Overseas privacy regulator, where a complaint relates to the actions of an overseas
                                    agency; and</li>
                                <li>Anyone else to whom you authorise us to disclose it.</li>
                            </ul>
                            <p class="text-justify pt-3">Except as described above, we will not disclose your personal
                                information without your written or oral consent, unless we are required to do so by
                                applicable law.</p>
