                        <div class="single-description-para" id="protecting-your-personal-information">
                            <h6>Protecting your personal information</h6>
                            <p class="text-justify">We will take reasonable steps to keep your personal information safe
                                from loss, unauthorised activity, or other misuse. Our software is subject to audits to
                                ensure it is continuing to meet security requirements. All data handled in our software
                                is encrypted in transit and during storage and can only be accessed over secure network
                                connections.</p>
                        </div>
                        <div class="single-description-para" id="storing-your-personal-information">
                            <h6>Storing your personal information</h6>
                            <p class="text-justify">We will only retain personal information as long as it is required
                                for the purposes for which the information may lawfully be used. All data stored online
                                is backed up and can be retrieved in the event of data loss or corruption.</p>
                            <p class="text-justify pt-3">Data will sometimes be held on-premise at 114 Hare Puke drive,
                                Flagstaff, Hamilton if it is provided to us outside of our software.</p>
                        </div>
                        <div class="single-description-para" id="accessing-and-correcting-your-personal-information">
                            <h6>Accessing and correcting your personal information</h6>
                            <p class="text-justify">Subject to certain grounds for refusal set out in the Act, you have
                                the right to access your readily retrievable personal information that we hold and to
                                request a correction to your personal information. Before you exercise this right, we
                                will need evidence to confirm that you are the individual to whom the personal
                                information relates.</p>
                            <p class="text-justify pt-3">In respect of a request for correction, if we think the
                                correction is reasonable and we are reasonably able to change the personal information,
                                we will make the correction. If we do not make the correction, we will take reasonable
                                steps to note the personal information that you requested the correction.</p>
                            <p class="text-justify pt-3">If you want to exercise either of the above rights, email us at
                                info@gmfinance.nz. Your email should provide evidence of who you are and set out the
                                details of your request (e.g. the personal information, or the correction, that you are
                                requesting).</p>
                        </div>
                        <div class="single-description-para" id="data-breaches">
                            <h6>Data Breaches</h6>
                            <p class="text-justify">Our Privacy Officer has processes and systems in place in the
                                unfortunate event of a data breach. If such an event occurs, we will promptly identify,
                                report and examine a personal data breach.</p>
                        </div>
                        <div class="single-description-para" id="internet-use">
                            <h6>Internet use</h6>
                            <p class="text-justify">While we take reasonable steps to maintain secure internet
                                connections, if you provide us with personal information over the internet, the
                                provision of that information is at your own risk.</p>
                            <p class="text-justify pt-3">If you follow a link on our website to another site, the owner
                                of that site will have its own privacy policy relating to your personal information. We
                                suggest you review that site’s privacy policy before you provide personal information.
                            </p>
                            <p class="text-justify pt-3">We use cookies (an alphanumeric identifier that we transfer to
                                your computer’s hard drive so that we can recognise your browser) to monitor your use of
                                the website. You may disable cookies by changing the settings on your browser, although
                                this may mean that you cannot use all of the features of the website.</p>
